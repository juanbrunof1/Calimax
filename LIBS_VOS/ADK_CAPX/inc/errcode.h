#ifndef ERRCODE_H_
#define ERRCODE_H_

#define	SUCCESS	0
#define	ERROR	-1

//COMM error codes
#define 		COMM_ERROR					-0x01000000
#define			COMM_OPEN_ERROR				COMM_ERROR - 0x0000001
#define			COMM_OPEN_BLOCK_ERROR		COMM_ERROR - 0x0000002
#define			COMM_CLOSE_ERROR			COMM_ERROR - 0x0000003
#define			COMM_READ_HANDLER_ERROR		COMM_ERROR - 0x0000004
#define			COMM_READ_ERROR				COMM_ERROR - 0x0000005
#define			COMM_WRITE_HANDLER_ERROR	COMM_ERROR - 0x0000006
#define			COMM_WRITE_ERROR			COMM_ERROR - 0x0000007

//DEBUG ERROR CODES
#define 		DBG_ERROR					-0x02000000
#define			ERR_DEBUG_NOT_FOUND			DBG_ERROR - 0x0000001
#define			ERR_DEBUG_NOT_SET			DBG_ERROR - 0x0000002
#define			ERR_OPEN_SOCKET				DBG_ERROR - 0x0000003
#define			ERR_OPEN_CONNECT			DBG_ERROR - 0x0000004
#define 		ERR_SOCK_HNDL				DBG_ERROR - 0x0000005
#define 		ERR_DEBUG_IP_NOT_FOUND		DBG_ERROR - 0x0000006
#define			ERR_DEBUG_PORT_NOT_FOUND	DBG_ERROR - 0x0000007

//FILE ERROR CODES
#define 		FILE_ERROR					-0x03000000
#define			ERR_FILE_BAD_HANDLER		FILE_ERROR - 0x0000001

#endif /* ERRCODE_H_ */
